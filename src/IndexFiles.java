/**
 * Index all text files under a directory.
 * <p>
 * This is a command-line application demonstrating simple Lucene indexing.
 * Run it with no command-line arguments for usage information.
 */
public class IndexFiles {

    private IndexFiles() {
    }

    /**
     * Index all text files under a directory.
     */
    public static void main(String[] args) {
        String indexPath = "index";

        Date start = new Date();
        try {

            Directory dir = FSDirectory.open(Paths.get(indexPath));
            Analyzer analyzer = new StandardAnalyzer();
            IndexWriterConfig iwc = new IndexWriterConfig(analyzer);
            iwc.setOpenMode(OpenMode.CREATE_OR_APPEND);

            // Optional: for better indexing performance, if you
            // are indexing many documents, increase the RAM
            // buffer.  But if you do this, increase the max heap
            // size to the JVM (eg add -Xmx512m or -Xmx1g):
            //
            // iwc.setRAMBufferSizeMB(256.0);

            IndexWriter writer = new IndexWriter(dir, iwc);
            Document doc = new Document();
            doc.add(new StringField("name", "selvan", Field.Store.YES));
            doc.add(new StringField("city", "bangalore", Field.Store.YES));
            writer.updateDocument(new Term("name", "selvan"), doc);

            doc = new Document();
            doc.add(new StringField("name", "thamarai", Field.Store.YES));
            doc.add(new StringField("country", "india", Field.Store.YES));
            writer.updateDocument(new Term("name", "thamarai"), doc);

            doc = new Document();
            doc.add(new StringField("org", "bitstat", Field.Store.YES));
            writer.updateDocument(new Term("name", "selvan"), doc);

            writer.close();

            Date end = new Date();
            System.out.println(end.getTime() - start.getTime() + " total milliseconds");

        } catch (IOException e) {
            System.out.println(" caught a " + e.getClass() +
                    "\n with message: " + e.getMessage());
        }
    }
}
